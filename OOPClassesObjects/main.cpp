/*
 * What is Object-Oriented Programming?
 *  -Classes and Objects
 *      -focus is on classes tha tmodel real-world domain entities
 *      -allows developers to think at higher level of abstraction
 *      -used successfully in very large programs
 *  -Encapsulation
 *      -objects contain data AND oeprations that work on that data
 *      -Abstracted Data Type (ADT)
 *  -Information-Hiding
 *      -implementation-specific logic can be hidden
 *      -users of the class code to the interface since they don't need to know
 *      the implementation
 *      -more abstraction
 *      -easier to test, debug, maintain, and extend
 *  -Reusability
 *      -easier to reuse classes in other applications
 *      -faster development
 *      -higher quality
 *  -Inheritance
 *      -can create new classes in terms of existing classes
 *      -reusability
 *      -polymorphic classes
 *  -Polymorhism and more...
 * 
 * Procedural Programming
 *  -Focus is on the processes or actions that the program takes
 *  -Programs are typically a collection of functions
 *  -Data is declared separately
 *  -Data is passed as arguments into functions
 *  -Fairly easy to learn
 * 
 * Prodedural Programming Limitations
 *  -Functions need to know the structure of the data
 *      -If the structure of teh data changes many functions must be changed
 *  -As programs get larger they become more:
 *      -Difficult to understand
 *      -Difficult to maintain
 *      -Difficult to extend
 *      -Difficult to debug
 *      -Difficult to reuse code
 *      -Fragile and easier to break
 *  
 * OO Programming concepts and their advantages
 * 
 * OO Programming limitations
 *  -Not a panacea (a solution or remedy for all difficults)
 *      -OO programming won't make bad code better
 *      -not suitable for all types of problems
 *      -not everything decomposes to a class
 *  -Learning Curve
 *      -usually a steeper learning curve, especially for C++
 *      -many OO languages, many variations of OO concepts
 *  -Design
 *      -usually more up-front design is necessary to create good models and hierarchies
 *  -Programs can be:
 *      -larger in size
 *      -slower
 *      -more complex
 * 
 * */

